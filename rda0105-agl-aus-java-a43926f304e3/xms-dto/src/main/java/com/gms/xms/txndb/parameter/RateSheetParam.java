package com.gms.xms.txndb.parameter;

/**
 * @author dattv
 */
public class RateSheetParam {
    private String zoneCode;
    private String zoneNo;
    private String sheetIdList;

    public String getZoneCode() {
        return zoneCode;
    }

    public void setZoneCode(String zoneCode) {
        this.zoneCode = zoneCode;
    }

    public String getZoneNo() {
        return zoneNo;
    }

    public void setZoneNo(String zoneNo) {
        this.zoneNo = zoneNo;
    }

    public String getSheetIdList() {
        return sheetIdList;
    }

    public void setSheetIdList(String sheetIdList) {
        this.sheetIdList = sheetIdList;
    }
}
